private DefaultCategoryDataset obtenerDatosProductosVendidos() {
    DefaultCategoryDataset dataset = new DefaultCategoryDataset();

    try (Connection conn = ConexionBD.getConexion()) {
        String sql = "SELECT p.nombre AS producto, SUM(dv.cantidad) AS cantidad_vendida " +
                     "FROM detalle_venta dv " +
                     "INNER JOIN productos p ON dv.id_producto = p.id " +
                     "GROUP BY p.nombre " +
                     "ORDER BY cantidad_vendida DESC";

        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            String producto = rs.getString("producto");
            int cantidad = rs.getInt("cantidad_vendida");
            dataset.addValue(cantidad, "Unidades Vendidas", producto);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al obtener datos: " + e.getMessage());
    }

    return dataset;
}


private void mostrarGrafica() {
    DefaultCategoryDataset dataset = obtenerDatosProductosVendidos();

    JFreeChart grafica = ChartFactory.createBarChart(
        "Productos MÃ¡s Vendidos", 
        "Producto",               
        "Cantidad Vendida",       
        dataset
    );

    ChartPanel panel = new ChartPanel(grafica);
    panel.setMouseWheelEnabled(true); 

    panelGrafica.setLayout(new java.awt.BorderLayout());
    panelGrafica.add(panel, java.awt.BorderLayout.CENTER);
    panelGrafica.validate();
}


