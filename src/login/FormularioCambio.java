    private void actualizarCambio() {
        try {
            double entregado = Double.parseDouble(txtEntregado.getText().trim());
            double cambio = entregado - totalVenta;

            if (cambio < 0) {
                lblCambio.setText("Cambio: Falta $" + String.format("%.2f", -cambio));
                lblCambio.setForeground(new java.awt.Color(204, 0, 0)); // rojo
            } else {
                lblCambio.setText("Cambio: $" + String.format("%.2f", cambio));
                lblCambio.setForeground(new java.awt.Color(0, 153, 0)); // verde
            }
        } catch (NumberFormatException e) {
            lblCambio.setText("Cambio: -");
            lblCambio.setForeground(new java.awt.Color(0, 0, 0)); // negro
        }
    }

