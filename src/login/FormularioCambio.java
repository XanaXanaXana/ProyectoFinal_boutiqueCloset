    private void btnFinalizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarCompraActionPerformed
        try {
            double entregado = Double.parseDouble(txtEntregado.getText().trim());
            double cambio = entregado - totalVenta;

            String correoCliente = txtCorreo.getText().trim();
            String tipoPago = (String) cmbSeleccion.getSelectedItem();
            String clave = txtClave.getText().trim();
            String claveParcial = (clave.length() >= 3) ? clave.substring(0, 3) + "*************" : "*************";

            if (correoCliente.isEmpty()) {
                JOptionPane.showMessageDialog(this, "❌ Ingrese el correo del cliente.");
                return;
            }

            if (tipoPago.equalsIgnoreCase("Tarjeta") && clave.isEmpty()) {
                JOptionPane.showMessageDialog(this, "❌ Ingrese la clave de la tarjeta.");
                return;
            }

            if (cambio < 0) {
                lblCambio.setText("Cambio: Falta $" + String.format("%.2f", -cambio));
                lblCambio.setForeground(new java.awt.Color(204, 0, 0));
            } else {
                lblCambio.setText("Cambio: $" + String.format("%.2f", cambio));
                lblCambio.setForeground(new java.awt.Color(0, 153, 0));
                if (listener != null) {
                    listener.onCambioCalculado(entregado, cambio, correoCliente, tipoPago, claveParcial);
                }
                dispose(); // ✅ Se cierra solo después de todo
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "❌ Ingrese una cantidad válida.");
        }
    }//GEN-LAST:event_btnFinalizarCompraActionPerformed

