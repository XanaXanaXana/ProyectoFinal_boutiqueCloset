    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
    String nombre = txtNombre.getText().trim();
    String correo = txtCorreo.getText().trim();
    String mensaje = txtMensaje.getText().trim();

    if (nombre.isEmpty() || correo.isEmpty() || mensaje.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Completa todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try (Connection conn = ConexionBD.getConexion()) {
        String sql = "INSERT INTO contacto_soporte (nombre, correo_cliente, mensaje) VALUES (?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, nombre);
        stmt.setString(2, correo);
        stmt.setString(3, mensaje);

        int rowsInserted = stmt.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "¡Mensaje guardado correctamente!", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            txtNombre.setText("");
            txtCorreo.setText("");
            txtMensaje.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo guardar el mensaje.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnEnviarActionPerformed

