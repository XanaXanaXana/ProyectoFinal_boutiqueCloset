    private void enviarCorreoEmpresa(String nombre, String correoCajero, String mensajeCajero) {
        final String correoEmpresa = "boutiquecloset6@gmail.com"; 
        final String claveCorreo = "qapr lixg tziz ljot"; 
        final String destinatario = "boutiquecloset6@gmail.com";

        String asunto = "Nuevo mensaje de contacto";
        String cuerpo = "Nombre: " + nombre + "\nCorreo: " + correoCajero + "\nMensaje:\n" + mensajeCajero;

        Properties props = new Properties();
        props.put("mail.smtp.host", "smtp.gmail.com");
        props.put("mail.smtp.port", "587");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.starttls.enable", "true");

        Session session = Session.getInstance(props, new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(correoEmpresa, claveCorreo);
            }
        });

        try {
            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(correoEmpresa));
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(destinatario));
            message.setSubject(asunto);
            message.setText(cuerpo);

            Transport.send(message);
            System.out.println("Correo enviado correctamente.");
        } catch (MessagingException e) {
            JOptionPane.showMessageDialog(this, "Error al enviar correo: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
