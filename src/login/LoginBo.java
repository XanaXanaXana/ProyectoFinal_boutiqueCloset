    private void btnComenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComenzarActionPerformed
        String correoUsuario = txtCorreo.getText().trim();
        String contrasenaUsuario = new String(txtContraseña.getPassword()).trim();
        String captchaIngresado = captcha.getText().trim();

        if (correoUsuario.isEmpty() || contrasenaUsuario.isEmpty() || captchaIngresado.isEmpty()) {
            lblResultado.setForeground(Color.RED);
            lblResultado.setText("❌ Completa todos los campos.");
            return;
        }

        if (!captchaIngresado.equalsIgnoreCase(captchaPanel1.getCodigo())) {
            lblResultado.setForeground(Color.RED);
            lblResultado.setText("❌ Captcha incorrecto.");
            captchaPanel1.generarCaptcha();
            captcha.setText(""); 
            return;
        }

        String resultadoLogin = validarLogin(correoUsuario, contrasenaUsuario);

        if (resultadoLogin.equalsIgnoreCase("Administrador")) {
            lblResultado.setForeground(Color.BLACK);
            lblResultado.setText("✅ Bienvenido Administrador");
            AceesoAdmi ventanaAdmin = new AceesoAdmi();
            ventanaAdmin.setVisible(true);
            this.dispose();
        } else if (resultadoLogin.equalsIgnoreCase("Cajero")) {
            lblResultado.setForeground(Color.BLACK);
            lblResultado.setText("✅ Bienvenido Cajero");
            AccesoClie ventanaCajero = new AccesoClie();
            ventanaCajero.setVisible(true);
            this.dispose();
        } else if (resultadoLogin.equals("CREDENCIALES_INVALIDAS")) {
            lblResultado.setForeground(Color.RED);
            lblResultado.setText("❌ Correo o contraseña incorrectos.");
        }
    }//GEN-LAST:event_btnComenzarActionPerformed

